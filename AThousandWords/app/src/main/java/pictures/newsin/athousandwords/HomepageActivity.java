/**
 * An example full-screen activity that shows and hides the system UI (i.e.
 * status bar and navigation/system bar) with user interaction.
 */
public class HomepageActivity extends AppCompatActivity {


    private static int[] NEWS_IDS = {
            R.id.news_picture_0x0, R.id.news_picture_1x0,
            R.id.news_picture_0x1, R.id.news_picture_1x1,
            R.id.news_picture_0x2, R.id.news_picture_1x2
    };

    private static final int UI_ANIMATION_DELAY = 0;
    private final Handler mHideHandler = new Handler();
    private final Runnable mHidePart2Runnable = new Runnable() {
        @SuppressLint("InlinedApi")
        @Override
        public void run() {
            // Delayed removal of status and navigation bar

            // Note that some of these constants are new as of API 16 (Jelly Bean)
            // and API 19 (KitKat). It is safe to use them, as they are inlined
            // at compile-time and do nothing on earlier devices.
            /*mContentView.setSystemUiVisibility(View.SYSTEM_UI_FLAG_LOW_PROFILE
                    | View.SYSTEM_UI_FLAG_FULLSCREEN
                    | View.SYSTEM_UI_FLAG_LAYOUT_STABLE
                    | View.SYSTEM_UI_FLAG_IMMERSIVE_STICKY
                    | View.SYSTEM_UI_FLAG_LAYOUT_HIDE_NAVIGATION
                    | View.SYSTEM_UI_FLAG_HIDE_NAVIGATION);*/
        }
    };
    private final Runnable mShowPart2Runnable = new Runnable() {
        @Override
        public void run() {
            // Delayed display of UI elements
            ActionBar actionBar = getSupportActionBar();
            if (actionBar != null) {
                actionBar.show();
            }
        }
    };

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

        requestWindowFeature(Window.FEATURE_NO_TITLE);
        getWindow().setFlags(WindowManager.LayoutParams.FLAG_FULLSCREEN,
                WindowManager.LayoutParams.FLAG_FULLSCREEN);
        setContentView(R.layout.activity_homepage);

        // PULL THE NEWSIN.PICTURES CONTENT
        new LoadNewsTask().execute();
    }

    @Override
    protected void onPostCreate(Bundle savedInstanceState) {
        super.onPostCreate(savedInstanceState);

        // Trigger the initial hide() shortly after the activity has been
        // created, to briefly hint to the user that UI controls
        // are available.
        hide();
    }

    private void hide() {
        // Hide UI first
        ActionBar actionBar = getSupportActionBar();
        if (actionBar != null) {
            actionBar.hide();
        }

        // Schedule a runnable to remove the status and navigation bar after a delay
        mHideHandler.removeCallbacks(mShowPart2Runnable);
        mHideHandler.postDelayed(mHidePart2Runnable, UI_ANIMATION_DELAY);
    }


    public Bitmap getBitmapFromURL(String src) {
        try {
            java.net.URL url = new java.net.URL(src);
            HttpURLConnection connection = (HttpURLConnection) url
                    .openConnection();
            connection.setDoInput(true);
            connection.connect();
            InputStream input = connection.getInputStream();
            Bitmap myBitmap = BitmapFactory.decodeStream(input);
            return myBitmap;
        } catch (IOException e) {
            e.printStackTrace();
            return null;
        }
    }

    public class LoadNewsTask extends AsyncTask<String, Void, List<Bitmap>> {

        @Override
        protected List<Bitmap> doInBackground(String... params) {

            JSONParser parser = new JSONParser();
            JSONObject result = parser.getJSONFromUrl("https://thousand-words.appspot.com/getnews");
            List<Bitmap> bitmaps = new ArrayList<Bitmap>();
            try {
                JSONObject nyt = result.getJSONObject("the-new-york-times");
                Bitmap image = getBitmapFromURL(nyt.getString("image"));
                bitmaps.add(image);
            }catch(JSONException e) {
                e.printStackTrace();
            }
            return bitmaps;
        }


        protected void onPostExecute(List<Bitmap> images) {
            for (int i = 0; i < images.size() && i < NEWS_IDS.length; i++) {
                ImageButton newsButton = (ImageButton) findViewById(NEWS_IDS[i]);
                newsButton.setImageBitmap(images.get(i));
            }
        }

    }
}
